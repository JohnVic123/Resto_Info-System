    private void rgsBttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rgsBttonActionPerformed
        String name = nameField.getText();
        String uname = unameField.getText();
        String password = passwField.getText();
        
        try{
                      
            FileOutputStream file = new FileOutputStream("Reg.txt", true);
            PrintWriter printer = new PrintWriter(file);
            
            printer.println(name + "," + uname + "," + password);
            JOptionPane.showMessageDialog(null, "REGISTERED SUCCESSFULLY");
            printer.close();
                     
            
            Login log = new Login();
            log.setVisible(true);
            this.dispose();
            
        }catch (HeadlessException | FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "REGISTRATION UNSUCCESSFULL.");
            
        }       
        nameField.setText("");
        unameField.setText("");
        passwField.setText("");
    }//GEN-LAST:event_rgsBttonActionPerformed

